public class CakeCase {
    public static void cake(int choice) {
        if (choice == 1) {
            System.out.println("Ban hay lua chon banh");
        } else if (choice == 2) {
            System.out.println("Ban da chon dung roi");
        } else {
            System.out.println("Khong co banh do dau");
        }
    }
}