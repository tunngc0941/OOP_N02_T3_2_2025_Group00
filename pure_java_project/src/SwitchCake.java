public class SwitchCake {
    public static void switchCase() {
        int choice = 2;
        switch (choice) {
            case 1:
                System.out.println("Ban chon banh Socola.");
                break;
            case 2:
                System.out.println("Ban da chon Banh Vani.");
                break;
            default:
                System.out.println("CHon sai roi chon lai di.");
        }
    }
}